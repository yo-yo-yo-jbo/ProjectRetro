#ifndef HeadTrackingSingletonComponent_h
#define HeadTrackingSingletonComponent_h

////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////

#include "../../ECS.h"

////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////

// World units the camera eye moves per unit of normalized head displacement.
// The overworld camera sits ~16.8 units back with a narrow FoV. The sidecar
// also amplifies the (small) real head motion via its --gain, so the effective
// eye travel is head * gain * scale. Tune to taste.
const float HEAD_LOOK_SCALE_X = 11.0f;
const float HEAD_LOOK_SCALE_Y = 7.0f;
const float HEAD_LOOK_SCALE_Z = 4.0f;

////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////

class HeadTrackingSingletonComponent final : public ecs::IComponent
{
public:
    int   mSocketFd = -1;      // UDP socket the sidecar publishes head data to
    bool  mEnabled  = true;    // toggle the whole effect
    float mHeadX    = 0.0f;    // normalized [-1, 1], smoothed (right = +)
    float mHeadY    = 0.0f;    // normalized [-1, 1], smoothed (up = +)
    float mHeadZ    = 0.0f;    // closeness proxy, smoothed (lean in = +)
};

////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////

#endif /* HeadTrackingSingletonComponent_h */
